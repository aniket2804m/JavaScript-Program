// Switch Case Statements:-
let color = "red";

switch (color) {
    case "red":
        console.log("stop");
        break;

    case "yellow":
        console.log("Slow down");
        break;

    case "green":
        console.log("Go");
        break;

    default:
        console.log("Broken Light");
}

// Practice Qs:- Use switch statement to print the day of the week using a number variable 'day'
let day = 8;

switch (day) {
    case 1:
        console.log("Monday");
        break;

    case 2:
        console.log("Tuesday");
        break;

    case 3:
        console.log("Wednesday");
        break;

    case 4:
        console.log("Tusday");
        break;

    case 5:
        console.log("Friday");
        break;

    case 6:
        console.log("Starday");
        break;

    case 7:
        console.log("Sunday, fun day");
        break;

    default:
        console.log("Wrong day!");
}